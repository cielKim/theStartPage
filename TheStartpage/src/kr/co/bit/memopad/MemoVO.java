package kr.co.bit.memopad;

public class MemoVO {
	
	//메모내용
	private String Memo;

	public MemoVO(String memo) {
		super();
		Memo = memo;
	}
	
	public String getMemo() {
		return Memo;
	}

	public void setMemo(String memo) {
		Memo = memo;
	}


	@Override
	public String toString() {
		return "MemoVO [Memo=" + Memo + "]";
	}
	
	
}
